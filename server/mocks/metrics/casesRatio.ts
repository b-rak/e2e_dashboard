export const getCasesRatio = (
  fromDate: string,
  toDate: string,
  dashboardId?: number,
  caseId?: number
) => {
  const diff = dateDifference(fromDate, toDate);
  if (dashboardId && caseId) {
    const filtered = ratios
      .filter(
        (ratio) => ratio.dashboardId === dashboardId && ratio.caseId === caseId
      )
      .slice(0, diff);
    return mergeObjects(filtered);
  } else {
    return mergeObjects(ratios);
  }
};

function dateDifference(date1: string, date2: string) {
  // Parse the dates and remove the time portion
  const d1 = new Date(date1.split("T")[0]);
  const d2 = new Date(date2.split("T")[0]);

  // Calculate the difference in milliseconds
  const diffTime = Math.abs(d2.getTime() - d1.getTime());

  // Convert milliseconds to days
  return Math.ceil(diffTime / (1000 * 60 * 60 * 24));
}

type DataObject = {
  dashboardId: number;
  caseId: number;
  createdDate?: string; // Optional since we are omitting it in the output
  passed: number;
  failed: number;
  warning: number;
  skipped: number;
  falsePositiveCount: number;
};

type MergedObject = Omit<DataObject, "createdDate">;

function mergeObjects(arr: DataObject[]): MergedObject[] {
  const mergedMap: Record<string, MergedObject> = {};

  arr.forEach((obj) => {
    const key = `${obj.dashboardId}-${obj.caseId}`;

    if (!mergedMap[key]) {
      mergedMap[key] = {
        dashboardId: obj.dashboardId,
        caseId: obj.caseId,
        passed: 0,
        failed: 0,
        warning: 0,
        skipped: 0,
        falsePositiveCount: 0,
      };
    }

    mergedMap[key].passed += obj.passed;
    mergedMap[key].failed += obj.failed;
    mergedMap[key].warning += obj.warning;
    mergedMap[key].skipped += obj.skipped;
    mergedMap[key].falsePositiveCount += obj.falsePositiveCount;
  });

  return Object.values(mergedMap);
}

